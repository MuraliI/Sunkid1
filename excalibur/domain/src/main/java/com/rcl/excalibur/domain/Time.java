package com.rcl.excalibur.domain;


public class Time {

    public String day;
    public String time;

    public Time() {
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}

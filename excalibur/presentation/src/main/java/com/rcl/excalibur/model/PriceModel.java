package com.rcl.excalibur.model;


public class PriceModel {

    public String title;
    public String value;

    public PriceModel() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}

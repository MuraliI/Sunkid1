package com.rcl.excalibur.model;


public class TimeModel {

    public String day;
    public String time;

    public TimeModel() {
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}

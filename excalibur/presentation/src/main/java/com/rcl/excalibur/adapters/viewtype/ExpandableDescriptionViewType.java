package com.rcl.excalibur.adapters.viewtype;

import com.rcl.excalibur.adapters.base.RecyclerViewConstants;
import com.rcl.excalibur.adapters.base.RecyclerViewType;


public class ExpandableDescriptionViewType implements RecyclerViewType {

    private String description;

    public ExpandableDescriptionViewType(String description) {
        this.description = description;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    @Override
    public int getViewType() {
        return RecyclerViewConstants.VIEW_TYPE_EXPANDABLE_DESCRIPTION;
    }
}
